    /**
     * Abre a tela do último item adicionado para edição dos dados
     *
     * @param evt
     */
    private void editarItem(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarItem
        Item o = getItem();
        if (o != null) {
            this.dispose();
            if (o.getTipo().equals(TipoItem.HQ)) {
                TelaCadastroHQ telaHQ = new TelaCadastroHQ(null, true, o.getId());
                telaHQ.setInterfaceListaDeDesejo(true);
                telaHQ.montaAlterarDados((HQ) o);
                telaHQ.setVisible(true);
            } else if (o.getTipo().equals(TipoItem.MIDIA)) {
                TelaCadastroMidia telaMidia = new TelaCadastroMidia(null, true, o.getId());
                telaMidia.setInterfaceListaDeDesejo(true);
                telaMidia.montaAlterarDados((Midia) o);
                telaMidia.setVisible(true);
            } else if (o.getTipo().equals(TipoItem.JOGOVIDEOGAME)) {
                TelaCadastroGame telaGame = new TelaCadastroGame(null, true, o.getId());
                telaGame.setInterfaceListaDeDesejo(true);
                telaGame.montaAlterarDados((Game) o);
                telaGame.setVisible(true);
            } else if (o.getTipo().equals(TipoItem.JOGOTABULEIRO)) {
                TelaCadastroTabuleiro telaTabuleiro = new TelaCadastroTabuleiro(null, true, o.getId());
                telaTabuleiro.setInterfaceListaDeDesejo(true);
                telaTabuleiro.montaAlterarDados((Tabuleiro) o);
                telaTabuleiro.setVisible(true);
            }
        }
    }//GEN-LAST:event_editarItem


    /**
     * Popula o os campos do item com os dados.
     *
     * @param o
     */
    private void populaItem(Item o) {
        lb_titulo.setText(o.getTitulo());
        lb_tipo.setText(o.getTipo().getTitulo());
        lb_nota.setText(String.valueOf(o.getNota()));
        lb_preco.setText(String.valueOf(o.getPrecoDeCompra()));
        lb_obs.setText(o.getObservacao());

    }

    /**
     * Valida campos
     *
     * @return
     */
    private boolean validaCampos() {
        if (getItem() != null) {
            return true;
        } else {
            return false;
        }
    }

    /**
     * Verifica se houve alteração de dados.
     *
     * @return <code>true</code> se houve alteração de dados ou
     * <code>false</code> caso não houve
     */
    private boolean isAlteracao() {
        if (!lb_titulo.equals("")) {
            return true;
        }
        return false;
    }

    /**
     * Recupera o item a ser cadastrado na lista de desejo.
     *
     * @return O item
     */
    public Item getItem() {
        return item;
    }

    /**
     * Seta o item a ser cadastrado na lista de desejo.
     *
     * @param item O item
     */
    public void setItem(Item item) {
        this.item = item;
    }

    /**
     * Recupera da interface grafica o objeto ListaDeDesejo
     *
     * @return o objeto ListaDeDesejo contendo o item a ser inserido
     */
    private ListaDeDesejo getInterfaceListaDeDesejo() {
        return new ListaDeDesejo(tf_data_lacamento.getText(), tf_site_compra.getText(), item);
    }

